#introduction
print("Hello! I'll calculate the amount of money everyone sharing the bill will have to pay.")
#gathering information and converting it into numbers
bill = float(input("How much was the bill?\n"))
people = int(input ("How many people are splitting the bill?\n"))
tip = float(input("What bill's percentage do you want to leave as a tip?\n"))
#calculating
each_total = (bill+(bill*tip/100))/people
final_amount = "{:.2f}".format(each_total)
print(f"Each person should pay: {final_amount}")
